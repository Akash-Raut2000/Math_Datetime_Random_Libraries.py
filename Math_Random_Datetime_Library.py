import math  # For mathematical functions
import random  # For generating random numbers
import datetime  # For working with dates and times

# Using the math module
square_root = math.sqrt(16)  # Calculates the square root of 16
print("Square root of 16:", square_root)

# Using the random module
random_number = random.randint(1, 10)  # Generates a random number between 1 and 10
print("Random number:", random_number)

# Using the datetime module
current_time = datetime.datetime.now()  # Gets the current date and time
print("Current date and time:", current_time)
